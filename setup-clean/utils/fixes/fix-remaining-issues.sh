          /restart:/ {
            a\    environment:
            a\      RLIMIT_NOFILE: "10000"
            a\      SEED_SELF_HOST: "true"
            a\    ulimits:
            a\      nofile:
            a\        soft: 10000
            a\        hard: 10000
